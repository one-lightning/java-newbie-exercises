package variables;

public class ReAssignVariables {

	public static void main(String[] args) {

		String season = "Winter";

		System.out.println("Current season : " + season);

		season = "Spring";

		System.out.println("Current season : " + season);

		season = "Summer";

		System.out.println("Current season : " + season);

		season = "Autumn";

		System.out.println("Current season : " + season);

	}

}
